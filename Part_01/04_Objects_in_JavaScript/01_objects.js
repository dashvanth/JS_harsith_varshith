// objects -> creates key value pairs, 

// how to create objects 
const person = {name: "dashvanth", age: 22};

// how to access key values / properties 
console.log(person);
console.log(person.name);
console.log(person.age);
// another way 
console.log(person["name"]);
console.log(person["age"]);
// how to add key value pair 
person.hobbies = "listening music", "touch typing";
console.log(person.hobbies);