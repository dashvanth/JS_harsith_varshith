// break -> used to stop current iteration 

for(let i=1;i<=10;i++){
    if(i==4){
        break;
    }
    console.log(i);
}

// continue -> used to stop current iteration and moves to next iteration(skips a particular iteration)

for(let i=1;i<=10;i++){
    if(i==5){
        continue;
    }
    console.log(i);
}