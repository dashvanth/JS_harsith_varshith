// boolean -> true or false 

let num1 = 5;
let num2 = 5;
console.log(num1>num2);
console.log(num1>=num2);
console.log(num1<num2);
console.log(num1==num2);

// comparision == or === 
// == compares only value 
let str = "5";
let no = 5;
console.log(str==no);

// === compares both type and values (strict comparision)
console.log(str===no);

let value1 = 6;
let value2 = "6";
console.log(value1 != value2);
console.log(value1 !== value2);